// Enum to represent the different display states
public enum ViewState {
	BOOT_MENU,	// Upon init() method being called
	IN_GAME,	// Playing the game
	PAUSE_MENU,	// Upon pressing the "enter" key
	ENDING,		// Game terminating
	DEBUG;		// View to rotate environment, for debugging 

}
